package com.example.connect;

public class User {
    private String name_str,email_str,password_str;

    public User(){

    }

    public String getName_str() {
        return name_str;
    }

    public void setName_str(String name_str) {
        this.name_str = name_str;
    }

    public String getEmail_str() {
        return email_str;
    }

    public void setEmail_str(String email_str) {
        this.email_str = email_str;
    }

    public String getPassword_str() {
        return password_str;
    }

    public void setPassword_str(String password_str) {
        this.password_str = password_str;
    }
}
